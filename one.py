import math
def area_of_a_cirele (rabius):
    area1 = math.pi* rabius
    return area1


def area_of_a_triangle(rulex,height):
    area2 = rulex *height/2
    return area2


def area_of_the_rectangle(length,width):
    area3 = length * width
    return area3


def square_area (oneside,herself):
    area4 = oneside * herself
    return area4
